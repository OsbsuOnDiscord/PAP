        <p>A PSU (Fonte de Alimentação), também conhecida como fonte de energia, é um componente essencial do
            computador, responsável por converter a corrente elétrica da tomada em energia utilizável pelos componentes
            internos. A PSU fornece diferentes voltagens para alimentar a motherboard, CPU, GPU, discos rígidos e outros
            dispositivos. A sua potência (medida em watts) e eficiência energética (como as certificações 80 Plus
            Bronze, Silver, Gold, etc.) são fatores críticos que determinam a estabilidade e o desempenho do sistema,
            especialmente em configurações mais exigentes, como PCs para gaming ou trabalho pesado.</p>
