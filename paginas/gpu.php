            <h2>Erro 1: Ecrã Preto ao Ligar o PC</h2>
            <hr>
            <p><strong>Descrição:</strong> O monitor não exibe imagem após ligar o computador.</p>
            <p><strong>Motivo:</strong> Este erro pode ocorrer devido a cabos de vídeo mal conectados, drivers
                desatualizados ou problemas na própria placa gráfica.</p>
            <p><strong>Correção rápida:</strong> Verifique se os cabos de vídeo estão bem conectados. Tente usar outro
                cabo ou porta HDMI/DisplayPort. Se o problema persistir, atualize os drivers da placa gráfica ou teste-a
                noutra máquina.</p>
        </div>
        <br>

        <hr>

        <div class="error">
            <hr>
            <h2>Erro 2: Artefactos Visuais no Ecrã</h2>
            <hr>
            <p><strong>Descrição:</strong> Aparecem distorções, linhas coloridas ou pixels aleatórios no ecrã.</p>
            <p><strong>Motivo:</strong> Este problema pode ser causado por sobreaquecimento, overclocking excessivo ou
                uma placa gráfica defeituosa.</p>
            <p><strong>Correção rápida:</strong> Certifique-se de que a ventoinha da placa gráfica está funcionando
                corretamente. Limpe qualquer poeira acumulada. Reduza as configurações de overclocking e monitore a
                temperatura.</p>
        </div>
        <br>

        <hr>

        <div class="error">
            <hr>
            <h2>Erro 3: Travamentos Durante Jogos</h2>
            <hr>
            <p><strong>Descrição:</strong> O jogo trava ou fecha inesperadamente durante a execução.</p>
            <p><strong>Motivo:</strong> Pode ser causado por falta de memória VRAM, drivers desatualizados ou
                incompatibilidade com o hardware.</p>
            <p><strong>Correção rápida:</strong> Atualize os drivers da placa gráfica para a versão mais recente. Reduza
                as configurações gráficas do jogo ou aumente a capacidade de RAM do sistema.</p>
        </div>
        <br>

        <hr>

        <div class="error">
            <hr>
            <h2>Erro 4: Ventoinha Ruidosa</h2>
            <hr>
            <p><strong>Descrição:</strong> A ventoinha da placa gráfica faz muito barulho durante o uso.</p>
            <p><strong>Motivo:</strong> Poeria acumulada, rolamentos desgastados ou configurações agressivas de
                refrigeração.</p>
            <p><strong>Correção rápida:</strong> Limpe a ventoinha cuidadosamente com ar comprimido. Verifique o
                software da placa gráfica para ajustar a velocidade da ventoinha.</p>
        </div>
        <br>

        <hr>

        <div class="error">
            <hr>
            <h2>Erro 5: Não Reconhecida no Sistema</h2>
            <hr>
            <p><strong>Descrição:</strong> O sistema operativo não detecta a placa gráfica.</p>
            <p><strong>Motivo:</strong> Drivers corrompidos, placa gráfica mal encaixada ou falha no hardware.</p>
            <p><strong>Correção rápida:</strong> Reinicie o computador e entre na BIOS para verificar se a placa gráfica
                está listada. Reinstale os drivers ou reencaixe a placa na motherboard.</p>
        </div>
